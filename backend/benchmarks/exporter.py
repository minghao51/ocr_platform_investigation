"""
Export benchmark results to markdown format.
Generates documentation similar to docs/reference/benchmark-results-cord.md
"""

from datetime import datetime
from typing import Any, Dict
from database import crud


async def export_benchmark_results(
    dataset: str = "cord",
    output_path: str | None = None,
    min_samples: int = 10,
) -> str:
    """
    Export benchmark results for a dataset to markdown format.

    Args:
        dataset: Dataset name to filter by
        output_path: Optional file path to write to (returns string if None)
        min_samples: Minimum sample count for a run to be included

    Returns:
        Markdown string of the benchmark report
    """
    # Get all runs for this dataset
    runs = await crud.list_benchmark_runs(limit=500, dataset=dataset)

    # Filter runs with completed results
    completed_runs = [
        r
        for r in runs
        if r.get("overall_accuracy") is not None
        and r.get("sample_count", 0) >= min_samples
    ]

    if not completed_runs:
        return (
            f"# {dataset.upper()} Benchmark Results\n\nNo benchmark data available.\n"
        )

    # Get latest run per model
    latest_by_model: Dict[tuple, Dict[str, Any]] = {}
    for run in completed_runs:
        key = (run["provider"], run["model"])
        if key not in latest_by_model:
            latest_by_model[key] = run
        elif run.get("started_at") and latest_by_model[key].get("started_at"):
            if run["started_at"] > latest_by_model[key]["started_at"]:
                latest_by_model[key] = run

    # Sort by accuracy
    sorted_runs = sorted(
        latest_by_model.values(), key=lambda x: x["overall_accuracy"] or 0, reverse=True
    )

    # Generate markdown
    md_lines = [
        f"# {dataset.upper()} Benchmark Results",
        "",
        f"> Date: {datetime.now().strftime('%Y-%m-%d')}",
        f"> Dataset: {dataset}",
        f"> Models: {len(sorted_runs)}",
        "> Scoring: Normalized string comparison + weighted item matching",
        "",
        "---",
        "",
        "## Summary",
        "",
        "| Rank | Model | Provider | Accuracy | Median | Samples | Avg Latency | Cost/1000 docs |",
        "|------|-------|----------|----------|--------|---------|-------------|----------------|",
    ]

    for idx, run in enumerate(sorted_runs):
        rank = idx + 1
        model = run["model"]
        provider = run["provider"]
        accuracy = run["overall_accuracy"] or 0
        samples = run["sample_count"]
        latency = run["avg_latency"] or 0
        cost = run["total_cost"] or 0
        cost_per_1000 = (cost / samples * 1000) if samples > 0 else 0

        md_lines.append(
            f"| {rank} | {model} | {provider} | **{accuracy:.1%}** | N/A | {samples} | {latency:.1f}s | ${cost_per_1000:.2f} |"
        )

    # Add recommendations
    md_lines.extend(
        [
            "",
            "### Recommendations",
            "",
            "**For Production Use:**",
        ]
    )

    if sorted_runs:
        best = sorted_runs[0]
        best_value = min(
            sorted_runs,
            key=lambda r: (
                (r["total_cost"] or 0)
                / max(r["sample_count"], 1)
                / (r["overall_accuracy"] or 0.01)
            ),
        )

        md_lines.extend(
            [
                f"- **Best accuracy**: {best['model']} ({best['provider']}) - {best['overall_accuracy']:.1%}",
                f"- **Best value**: {best_value['model']} ({best_value['provider']})",
            ]
        )

    # Add methodology
    md_lines.extend(
        [
            "",
            "---",
            "",
            "## Methodology",
            "",
            f"- **Dataset**: {dataset}",
            f"- **Total models tested**: {len(sorted_runs)}",
            "- **Scoring**: Field-level comparison with normalized strings",
            "- **Item matching**: Name-weighted (60%) with best-match bipartite assignment",
            "",
            f"*Generated by OCR Platform benchmark exporter on {datetime.now().strftime('%Y-%m-%d %H:%M')}*",
        ]
    )

    markdown = "\n".join(md_lines)

    if output_path:
        with open(output_path, "w") as f:
            f.write(markdown)

    return markdown


async def export_detailed_comparison(
    dataset: str = "cord",
    output_path: str | None = None,
) -> str:
    """
    Export detailed comparison with per-sample analysis.

    Args:
        dataset: Dataset name to filter by
        output_path: Optional file path to write to

    Returns:
        Markdown string with detailed analysis
    """
    runs = await crud.list_benchmark_runs(limit=500, dataset=dataset)

    completed_runs = [r for r in runs if r.get("overall_accuracy") is not None]

    if not completed_runs:
        return f"# {dataset.upper()} Detailed Analysis\n\nNo data available.\n"

    # Get latest per model
    latest_by_model: Dict[tuple, Dict[str, Any]] = {}
    for run in completed_runs:
        key = (run["provider"], run["model"])
        if key not in latest_by_model or (
            run.get("started_at")
            and latest_by_model[key].get("started_at")
            and run["started_at"] > latest_by_model[key]["started_at"]
        ):
            latest_by_model[key] = run

    sorted_runs = sorted(
        latest_by_model.values(), key=lambda x: x["overall_accuracy"] or 0, reverse=True
    )

    md_lines = [
        f"# {dataset.upper()} Detailed Benchmark Analysis",
        "",
        f"> Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}",
        "",
        "## Model Performance Breakdown",
        "",
    ]

    for run in sorted_runs[:10]:  # Top 10
        run_id = run["id"]
        model = run["model"]
        provider = run["provider"]
        accuracy = run["overall_accuracy"] or 0
        latency = run["avg_latency"] or 0
        cost = run["total_cost"] or 0

        md_lines.extend(
            [
                f"### {provider} / {model}",
                "",
                f"- **Accuracy**: {accuracy:.2%}",
                f"- **Avg Latency**: {latency:.2f}s",
                f"- **Total Cost**: ${cost:.4f}",
                f"- **Samples**: {run['sample_count']}",
                "",
            ]
        )

        # Get sample results for this run
        results = await crud.get_benchmark_results(run_id)

        if results:
            # Calculate distribution
            high_accuracy = sum(
                1 for r in results if (r.get("accuracy_score") or 0) >= 0.75
            )
            mid_accuracy = sum(
                1 for r in results if 0.5 <= (r.get("accuracy_score") or 0) < 0.75
            )
            low_accuracy = sum(
                1 for r in results if (r.get("accuracy_score") or 0) < 0.5
            )

            md_lines.extend(
                [
                    "**Accuracy Distribution:**",
                    f"- High (≥75%): {high_accuracy} samples",
                    f"- Medium (50-75%): {mid_accuracy} samples",
                    f"- Low (<50%): {low_accuracy} samples",
                    "",
                ]
            )

    markdown = "\n".join(md_lines)

    if output_path:
        with open(output_path, "w") as f:
            f.write(markdown)

    return markdown
